package com.kanban.common

/**
 * Базовый интерфейс для всех операций бизнес-логики.
 * Реализует паттерн Command: каждая операция принимает аргумент и возвращает результат.
 *
 * @param A тип входящего аргумента операции
 * @param R тип результата операции
 */
interface Operation<in A, out R> {
    /**
     * Выполняет операцию с переданным аргументом.
     *
     * @param arg входящий аргумент операции
     * @return результат выполнения операции
     */
    suspend fun execute(arg: A): R
}
